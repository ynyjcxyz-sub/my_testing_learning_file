public class sixthKey {
    int x;

    // Constructor with a parameter
    public sixthKey(int x) {
        this.x = x;
    }

    // Call the constructor
    public static void main(String[] args) {
        sixthKey myObj = new sixthKey(5);
        System.out.println("Value of x = " + myObj.x);
        //here
    }
}
